let currentSessionTheme: AfricanTheme | null = null;

export function getRandomTheme(): AfricanTheme {
  if (currentSessionTheme) {
    return currentSessionTheme;
  }

  const lastIndex = sessionStorage.getItem("africanThemeLastIndex");
  let randomIndex: number;

  do {
    randomIndex = Math.floor(Math.random() * africanThemes.length);
  } while (africanThemes.length > 1 && lastIndex !== null && randomIndex === parseInt(lastIndex, 10));

  sessionStorage.setItem("africanThemeLastIndex", randomIndex.toString());
  currentSessionTheme = africanThemes[randomIndex];
  return currentSessionTheme;
}

export function applyTheme(theme: AfricanTheme, isDark: boolean): void {
  const root = document.documentElement;
  const colors = isDark ? theme.dark : theme.light;
  
  root.style.setProperty("--primary", colors.primary);
  root.style.setProperty("--secondary", colors.secondary);
  root.style.setProperty("--accent", colors.accent);
  root.style.setProperty("--background", colors.background);
  root.style.setProperty("--card", colors.card);
  
  root.style.setProperty("--sidebar-primary", colors.primary);
  root.style.setProperty("--ring", colors.primary);
}
